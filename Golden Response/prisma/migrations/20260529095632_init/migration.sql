-- ===========================================================================
-- Full-text search: GIN index on the tsvector, plus a trigger that keeps it
-- in sync on every INSERT/UPDATE. Title is weighted A, excerpt B, body C.
-- (Body is stored as sanitized HTML; we strip tags before tokenizing.)
-- ===========================================================================
CREATE INDEX "Post_searchVector_idx" ON "Post" USING GIN ("searchVector");

CREATE OR REPLACE FUNCTION post_search_vector_update() RETURNS trigger AS $$
BEGIN
  NEW."searchVector" :=
    setweight(to_tsvector('english', coalesce(NEW.title, '')), 'A') ||
    setweight(to_tsvector('english', coalesce(NEW.excerpt, '')), 'B') ||
    setweight(to_tsvector('english',
      regexp_replace(coalesce(NEW."contentHtml", ''), '<[^>]+>', ' ', 'g')), 'C');
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER post_search_vector_trigger
BEFORE INSERT OR UPDATE OF title, excerpt, "contentHtml" ON "Post"
FOR EACH ROW EXECUTE FUNCTION post_search_vector_update();
